package wipro.day10.model;

public class Employee {
	private int epID;
	private String epName;
	private String epDepartment;
	
	public int getEpID() {
		return epID;
	}
	public void setEpID(int epID) {
		this.epID = epID;
	}
	public String getEpName() {
		return epName;
	}
	public void setEpName(String epName) {
		this.epName = epName;
	}
	public String getEpDepartment() {
		return epDepartment;
	}
	public void setEpDepartment(String epDepartment) {
		this.epDepartment = epDepartment;
	}
}
